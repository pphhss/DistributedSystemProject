
class Node():
    
    def __init__(self,_idx,_ip):
        self.__idx = _idx
        self.__ip = _ip

    def getIdx(self):
        return self.__idx

    def getIp(self):
        return self.__ip

    def setIdx(self,_idx):
        self.__idx = _idx

    def setIp(self,_ip):
        self.__ip = _ip

